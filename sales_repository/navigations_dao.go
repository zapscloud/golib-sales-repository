// NavigationDao - Card DAO Repository
type NavigationDao interface {
	// InitializeDao
	InitializeDao(client utils.Map, businessId string)
	//List - List all Collections
	List(filter string, sort string, skip int64, limit int64) (utils.Map, error)
	// Get - Get by code
	Get(navigationId string) (utils.Map, error)
	// Find - Find by filter
	Find(filter string) (utils.Map, error)
	// Create - Create Collection
	Create(indata utils.Map) (utils.Map, error)
	// Update - Update Collection
	Update(navigationId string, indata utils.Map) (utils.Map, error)
	// Delete - Delete Collection
	Delete(navigationId string) (int64, error)
}

// NewNavigationDao - Contruct Business Navigation Dao
func NewNavigationDao(client utils.Map, business_id string) NavigationDao {
	var daoNavigation NavigationDao = nil

	// Get DatabaseType and no need to validate error
	// since the dbType was assigned with correct value after dbService was created
	dbType, _ := db_common.GetDatabaseType(client)

	switch dbType {
	case db_common.DATABASE_TYPE_MONGODB:
		daoNavigation = &mongodb_repository.NavigationMongoDBDao{}
	case db_common.DATABASE_TYPE_ZAPSDB:
		// *Not Implemented yet*
	case db_common.DATABASE_TYPE_MYSQLDB:
		// *Not Implemented yet*
	}

	if daoNavigation != nil {
		// Initialize the Dao
		daoNavigation.InitializeDao(client, business_id)
	}

	return daoNavigation
}
